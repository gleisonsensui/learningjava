public class OperadoresAritimeticos {
    public static void main(String[] args) {
        
        int numeroOp = 10;
        
        int somaOp = numeroOp + 10;
        System.out.println(somaOp);

        int subOp = numeroOp - 5;
        System.out.println(subOp);

        int multOp = numeroOp * 2;
        System.out.println(multOp);

        int divOp = numeroOp / 2;
        System.out.println(divOp);

        int restOp = numeroOp % 7;
        System.out.println(restOp);



        
    }
}
