public class OperadoresAtribuicao {
    public static void main(String[] args) {
        // Operador de atribuição
        int numeroAtr = 10;
        System.out.println(numeroAtr);
        
        // Atribuicao Aditiva
        int somaAtr = numeroAtr += 10;
        System.out.println(somaAtr);
        
        // Atribuição subtrativa
        int subAtr = numeroAtr -= 5;
        System.out.println(subAtr);
        
        // Atribuição Multiplicativa
        int multAtr = numeroAtr *= 2;
        System.out.println(multAtr);
        
        // Atribuição Divisiva
        int divAtr = numeroAtr /=2;
        System.out.println(divAtr);

        // Atribuição resto
        int restAtr = numeroAtr %= 2;
        System.out.println(restAtr);



    }
}
