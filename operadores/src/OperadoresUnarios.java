public class OperadoresUnarios {
    public static void main(String[] args) {
        // Operador unario para transformar um valor positivo
        int positivoValue = 5;
        System.out.println(positivoValue);

        int negativoValue = -positivoValue;
        System.out.println(negativoValue);

        int incrementoValue = positivoValue++;
        System.out.println(incrementoValue);

        int decrementValue = positivoValue--;
        System.out.println(decrementValue);


    }

}
